import 'package:flutter/material.dart';

class Tile {
  String? image;
  Alignment? alignment;

  Tile({this.image, this.alignment});

  Widget croppedImageTile() {
    return FittedBox(
      fit: BoxFit.fill,
      child: ClipRect(
        child: Container(
          child: Align(
            alignment: this.alignment,
            widthFactor: 0.3,
            heightFactor: 0.3,
            child: Image.network(this.image),
          ),
        ),
      ),
    );
  }
}

